/* -------------------------------------------------------
   PROMPT CACHE - stores last 2 successful DALL-E prompts
   so similar inputs reuse a proven design spec
------------------------------------------------------- */
interface CachedPrompt {
  cacheKey: string;        // hash of user input params
  designSpec: string;      // the detailed design spec generated by GPT-4
  dallePromptBase: string; // compact base prompt used for DALL-E
  timestamp: number;
}

const promptCache: CachedPrompt[] = []; // max 2 entries

function makeCacheKey(options: GenerateJerseyOptions): string {
  const { prompt, preset, playerName, playerNumber, teamName, customInstructions } = options;
  return [prompt, preset, playerName, playerNumber, teamName, customInstructions]
    .map(v => (v || "").toLowerCase().trim())
    .join("|");
}

function getCachedPrompt(options: GenerateJerseyOptions): CachedPrompt | null {
  const key = makeCacheKey(options);
  return promptCache.find(c => c.cacheKey === key) || null;
}

function saveToCache(options: GenerateJerseyOptions, designSpec: string, dallePromptBase: string) {
  const key = makeCacheKey(options);
  // Remove existing entry for same key if present
  const idx = promptCache.findIndex(c => c.cacheKey === key);
  if (idx !== -1) promptCache.splice(idx, 1);
  // Keep max 2 entries
  if (promptCache.length >= 2) promptCache.shift();
  promptCache.push({ cacheKey: key, designSpec, dallePromptBase, timestamp: Date.now() });
  console.log(`[Prompt Cache] Saved. Cache size: ${promptCache.length}`);
}

/* -------------------------------------------------------
   JERSEY MASK LOADER - reads front/back mask files and
   converts them to base64 for GPT-4 Vision reference
------------------------------------------------------- */
function loadMaskAsBase64(maskRelPath: string): string | null {
  try {
    // Resolve from backend root -> frontend/public
    const fullPath = path.resolve(__dirname, "../../../../frontend/public", maskRelPath);
    if (!fs.existsSync(fullPath)) {
      console.warn(`[Mask Loader] File not found: ${fullPath}`);
      return null;
    }
    const buf = fs.readFileSync(fullPath);
    return buf.toString("base64");
  } catch (e) {
    console.warn("[Mask Loader] Error loading mask:", e);
    return null;
  }
}

