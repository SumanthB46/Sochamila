interface GenerateJerseyOptions {
  prompt: string;
  customInstructions?: string;
  preset?: JerseyPresetKey;
  sourceFiles?: string[]; // Array of base64 encoded reference images
  playerName?: string;
  playerNumber?: string;
  teamName?: string;
}

interface JerseyGenerationResult {
  success: boolean;
  images: string[];
  prompt: string;
  model: string;
  preset?: string | null;
  sourceFilesAnalyzed?: number;
  fromCache?: boolean;
}

/**
 * Build a short user-readable spec for GPT-4 analysis
 */
function buildEnhancedPrompt(options: GenerateJerseyOptions): string {
  const { prompt, customInstructions, preset, playerName, playerNumber, teamName } = options;
  let presetLine = "";
  if (preset && JERSEY_PRESETS[preset]) {
    const p = JERSEY_PRESETS[preset];
    presetLine = ` | Preset: ${p.name} - ${p.style}, collar: ${p.collar}, fabric: ${p.fabric}, pattern: ${p.pattern}`;
  }
  const parts: string[] = [];
  if (playerName) parts.push(`Player: ${playerName}`);
  if (playerNumber) parts.push(`Number: ${playerNumber}`);
  if (teamName) parts.push(`Team: ${teamName}`);
  return `${prompt}${presetLine}${parts.length ? " | " + parts.join(", ") : ""}${customInstructions ? " | " + customInstructions : ""}`;
}

/**
 * Build compact DALL-E base prompt (hard capped at 3400 chars)
 */
function buildDallEPromptBase(options: GenerateJerseyOptions): string {
  const { prompt, customInstructions, preset, playerName, playerNumber, teamName } = options;
  const parts: string[] = [];
  if (playerName) parts.push(`Player: ${playerName}`);
  if (playerNumber) parts.push(`Number: ${playerNumber}`);
  if (teamName) parts.push(`Team: ${teamName}`);
  let presetLine = "";
  if (preset && JERSEY_PRESETS[preset]) {
    const p = JERSEY_PRESETS[preset];
    presetLine = `\nStyle: ${p.style}. Collar: ${p.collar}. Pattern: ${p.pattern}.`;
  }
  let core = `Photorealistic football jersey. Flat-lay on pure white background. No human, no mannequin. Product mockup.\nDESIGN: ${prompt}${presetLine}`;
  if (parts.length) core += `\n${parts.join(", ")}`;
  if (customInstructions) core += `\n${customInstructions.slice(0, 150)}`;
  return core.slice(0, 3400);
}

/**
 * Step 0: Generate a single unified design spec via GPT-4 Vision.
 * Uses jersey mask templates as shape reference + user source images.
 * Returns a compact spec (~400 chars) shared between front and back DALL-E prompts.
 */
async function generateUnifiedDesignSpec(
  openai: OpenAI,
  options: GenerateJerseyOptions,
  maskFrontBase64: string | null,
  maskBackBase64: string | null,
  sourceAnalysis: string,
  maskLeftBase64?: string | null,
  maskRightBase64?: string | null
): Promise<string> {
  const userDesc = buildEnhancedPrompt(options);
  const imageContents: any[] = [];
  if (maskFrontBase64) {
    imageContents.push({ type: "image_url", image_url: { url: `data:image/webp;base64,${maskFrontBase64}`, detail: "low" } });
  }
  if (maskBackBase64) {
    imageContents.push({ type: "image_url", image_url: { url: `data:image/webp;base64,${maskBackBase64}`, detail: "low" } });
  }
  if (maskLeftBase64) {
    imageContents.push({ type: "image_url", image_url: { url: `data:image/webp;base64,${maskLeftBase64}`, detail: "low" } });
  }
  if (maskRightBase64) {
    imageContents.push({ type: "image_url", image_url: { url: `data:image/webp;base64,${maskRightBase64}`, detail: "low" } });
  }
  try {
    const response = await openai.chat.completions.create({
      model: "gpt-4o",
      messages: [
        {
          role: "system",
          content: "You are a football jersey design expert. Given a user request and optional reference images, produce a SINGLE detailed design spec (max 500 chars) that defines: exact primary/secondary colors (use specific color names), sleeve pattern geometry (describe EXACT shapes: zigzag, chevrons, stripes, etc.), collar style, fabric texture, logo/text placement. CRITICAL: Describe sleeve patterns in detail so they can be replicated identically across all 4 views. Be precise about pattern colors and geometry. This spec ensures all views show the SAME jersey."
        },
        {
          role: "user",
          content: [
            { type: "text", text: `User request: ${userDesc}${sourceAnalysis ? `\nReference: ${sourceAnalysis.slice(0, 200)}` : ""}\n\nOutput unified design spec:` },
            ...imageContents
          ]
        }
      ],
      max_tokens: 200
    });
    const spec = response.choices[0]?.message?.content?.trim() || "";
    return spec.slice(0, 500) || userDesc.slice(0, 500);
  } catch (e) {
    console.warn("[Design Spec] GPT-4 failed, using user prompt:", e);
    return userDesc.slice(0, 400);
  }
}

/**
 * Analyze source/reference images using GPT-4 Vision
 * Returns design insights based on reference images
 */
async function analyzeSourceImages(
  openai: OpenAI,
  sourceFiles: string[]
): Promise<string> {
  if (!sourceFiles || sourceFiles.length === 0) {
    return "";
  }

  try {
    const imageContents = sourceFiles.map(base64 => ({
      type: "image_url" as const,
      image_url: {
        url: `data:image/png;base64,${base64}`,
        detail: "high" as const
      }
    }));

    const response = await openai.chat.completions.create({
      model: "gpt-4o",
      messages: [
        {
          role: "system",
          content: `You are a design analysis expert. Analyze the provided reference images and extract key design elements that should be incorporated into a football jersey design. Focus on:
- Color schemes and palettes
- Patterns and textures
- Design style (modern, classic, aggressive, minimal, etc.)
- Any unique visual elements
- Overall aesthetic and mood

Provide a concise summary of design recommendations based on these reference images.`
        },
        {
          role: "user",
          content: [
            {
              type: "text",
              text: "Analyze these reference images and provide design recommendations for a football jersey:"
            },
            ...imageContents
          ]
        }
      ],
      max_tokens: 500
    });

    const analysis = response.choices[0]?.message?.content || "";
    return analysis ? `\n\nREFERENCE IMAGE ANALYSIS:\n${analysis}\nIncorporate these design elements into the jersey.` : "";
  } catch (error) {
    console.warn("[Source Image Analysis] Failed to analyze reference images:", error);
    return "";
  }
}

