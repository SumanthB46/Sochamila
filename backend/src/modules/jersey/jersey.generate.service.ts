/**
 * Generate jersey images using OpenAI DALL-E 3
 * Flow: check cache → load masks → GPT-4 unified design spec → DALL-E front → DALL-E back → cache save
 */
export async function generateJerseyImages(
  options: GenerateJerseyOptions
): Promise<JerseyGenerationResult> {
  const { preset, sourceFiles, playerName, playerNumber } = options;

  const apiKey = process.env.OPENAI_API_KEY;
  if (!apiKey) {
    throw new Error(
      "OPENAI_API_KEY is not configured. Get a key at https://platform.openai.com/api-keys and add it to your .env file."
    );
  }

  const openai = new OpenAI({ apiKey });

  try {
    // ── Step 1: Check prompt cache ────────────────────────────────────────
    const cached = getCachedPrompt(options);
    let designSpec: string;
    let dalleBase: string;
    let fromCache = false;

    if (cached) {
      console.log(`[Jersey Generation] Cache HIT - reusing design spec`);
      designSpec = cached.designSpec;
      dalleBase = cached.dallePromptBase;
      fromCache = true;
    } else {
      // ── Step 2: Analyze user-provided source files ───────────────────────
      let sourceAnalysis = "";
      if (sourceFiles && sourceFiles.length > 0) {
        console.log(`[Jersey Generation] Analyzing ${sourceFiles.length} reference image(s)...`);
        sourceAnalysis = await analyzeSourceImages(openai, sourceFiles);
      }

      // ── Step 3: Load jersey mask templates as shape reference ─────────────
      console.log(`[Jersey Generation] Loading jersey mask templates...`);
      const maskFront = loadMaskAsBase64("masks/jersey/front.webp");
      const maskBack  = loadMaskAsBase64("masks/jersey/back.webp");
      const maskLeft  = loadMaskAsBase64("masks/jersey/left.webp");
      const maskRight = loadMaskAsBase64("masks/jersey/right.webp");
      console.log(`[Jersey Generation] Masks loaded - front: ${!!maskFront}, back: ${!!maskBack}, left: ${!!maskLeft}, right: ${!!maskRight}`);

      // ── Step 4: Generate unified design spec via GPT-4 Vision ─────────────
      console.log(`[Jersey Generation] Generating unified design spec...`);
      designSpec = await generateUnifiedDesignSpec(openai, options, maskFront, maskBack, sourceAnalysis, maskLeft, maskRight);
      console.log(`[Jersey Generation] Design spec (${designSpec.length} chars): ${designSpec.slice(0, 100)}...`);

      // Build compact prompt base (will be shared for front + back)
      dalleBase = buildDallEPromptBase(options);
    }

    // ── Steps 5-8: Generate views sequentially for better control ─────────
    console.log(`[Jersey Generation] Generating FRONT view first...`);
    
    const baseSpec = `You are a professional sports apparel designer. Generate a photorealistic football jersey product mockup.

DESIGN SPEC (follow EXACTLY):
${designSpec}

CRITICAL REQUIREMENTS:
- Pure white background (#FFFFFF) on all views
- No shadows, no mannequin, no props
- Professional product photography quality
- Flat-lay overhead view
- Same lighting and fabric texture across all views`;

    // Generate FRONT view first
    const frontResponse = await openai.images.generate({
      model: "gpt-image-1",
      prompt: `${baseSpec}\n\nFRONT VIEW: Show the complete front of the football jersey. Display collar, both sleeves with patterns clearly visible, chest area with logos/sponsors. The LEFT sleeve is on the left side and RIGHT sleeve is on the right side when viewing from front.`,
      n: 1, size: "1024x1024", quality: "high",
    } as any);

    const frontB64 = (frontResponse.data?.[0] as any)?.b64_json;
    if (!frontB64) throw new Error("No image data received for front view.");

    // Analyze the generated front image to extract exact design details
    console.log(`[Jersey Generation] Analyzing front view for consistent back generation...`);
    const frontAnalysis = await analyzeFrontJerseyDesign(openai, frontB64);
    console.log(`[Jersey Generation] Front analysis: ${frontAnalysis.slice(0, 150)}...`);

    // Generate BACK view using front analysis
    console.log(`[Jersey Generation] Generating BACK view...`);
    const backResponse = await openai.images.generate({
      model: "gpt-image-1",
      prompt: `${baseSpec}\n\nBACK VIEW: Show the complete back of the SAME football jersey. Player name "${playerName || 'PLAYER'}" bold at top center. Number "${playerNumber || '00'}" large centered below. Use this exact design from the front: ${frontAnalysis}. Ensure colors, patterns, and fabric match perfectly.`,
      n: 1, size: "1024x1024", quality: "high",
    } as any);

    const backB64 = (backResponse.data?.[0] as any)?.b64_json;
    if (!backB64) throw new Error("No image data received for back view.");

    // Generate LEFT side view - be extremely explicit
    console.log(`[Jersey Generation] Generating LEFT SIDE view...`);
    const leftResponse = await openai.images.generate({
      model: "gpt-image-1",
      prompt: `${baseSpec}\n\nLEFT SIDE VIEW: Rotate the jersey to show ONLY the LEFT half. The LEFT sleeve must be centered and fully visible. The RIGHT sleeve must be completely hidden behind the jersey. This is the sleeve that appears on the LEFT side when viewing the front. Show exact same patterns and colors.`,
      n: 1, size: "1024x1024", quality: "high",
    } as any);

    const leftB64 = (leftResponse.data?.[0] as any)?.b64_json;
    if (!leftB64) throw new Error("No image data received for left view.");

    // Generate RIGHT side view - be extremely explicit
    console.log(`[Jersey Generation] Generating RIGHT SIDE view...`);
    const rightResponse = await openai.images.generate({
      model: "gpt-image-1",
      prompt: `${baseSpec}\n\nRIGHT SIDE VIEW: Rotate the jersey to show ONLY the RIGHT half. The RIGHT sleeve must be centered and fully visible. The LEFT sleeve must be completely hidden behind the jersey. This is the sleeve that appears on the RIGHT side when viewing the front. Show exact same patterns and colors.`,
      n: 1, size: "1024x1024", quality: "high",
    } as any);

    const rightB64 = (rightResponse.data?.[0] as any)?.b64_json;
    if (!rightB64) throw new Error("No image data received for right view.");

    // ── Step 9: Stitch 4 views into 2×2 grid using sharp ─────────────────
    console.log(`[Jersey Generation] Stitching 4 views into 2×2 grid...`);
    const resizeOpts = { fit: "contain" as const, background: { r: 255, g: 255, b: 255, alpha: 1 } };
    const [frontResized, backResized, leftResized, rightResized] = await Promise.all([
      sharp(Buffer.from(frontB64, "base64")).resize(1024, 1024, resizeOpts).toBuffer(),
      sharp(Buffer.from(backB64,  "base64")).resize(1024, 1024, resizeOpts).toBuffer(),
      sharp(Buffer.from(leftB64,  "base64")).resize(1024, 1024, resizeOpts).toBuffer(),
      sharp(Buffer.from(rightB64, "base64")).resize(1024, 1024, resizeOpts).toBuffer(),
    ]);

    // 2×2 grid: top-left=Front, top-right=Back, bottom-left=Left, bottom-right=Right
    const combinedBuffer = await sharp({
      create: { width: 2048, height: 2048, channels: 4, background: { r: 255, g: 255, b: 255, alpha: 1 } }
    })
      .composite([
        { input: frontResized, left: 0,    top: 0    },
        { input: backResized,  left: 1024, top: 0    },
        { input: leftResized,  left: 0,    top: 1024 },
        { input: rightResized, left: 1024, top: 1024 },
      ])
      .png()
      .toBuffer();

    const combinedBase64 = combinedBuffer.toString("base64");

    // ── Step 8: Upload to ImageKit ────────────────────────────────────────
    console.log(`[Jersey Generation] Uploading combined image to ImageKit...`);
    const timestamp = Date.now();
    const uploadResult = await imagekit.upload({
      file: combinedBase64,
      fileName: `jersey-combined-${preset || 'custom'}-${timestamp}.png`,
      folder: "/jersey-designs",
      tags: ["jersey", "combined-view", "gpt-image-1", preset || "custom"],
    });

    // ── Step 9: Save to cache ─────────────────────────────────────────────
    if (!fromCache) {
      saveToCache(options, designSpec, dalleBase);
    }

    console.log(`[Jersey Generation] Done! fromCache: ${fromCache}`);

    return {
      success: true,
      images: [uploadResult.url],
      prompt: designSpec,
      model: "gpt-image-1",
      preset: preset || null,
      sourceFilesAnalyzed: sourceFiles?.length || 0,
      fromCache,
    } as JerseyGenerationResult;

  } catch (err: any) {
    console.error("[Jersey Generation Error] status:", err?.status);
    console.error("[Jersey Generation Error] message:", err?.message);

    if (err?.status === 429 || err?.code === "rate_limit_exceeded" || err?.message?.includes("rate limit")) {
      const e = new Error(`OpenAI rate limit exceeded. Please try again in a minute.`);
      (e as any).status = 429;
      throw e;
    }
    if (err?.status === 401 || err?.message?.includes("Incorrect API key") || err?.message?.includes("Invalid API key")) {
      const e = new Error("Invalid OpenAI API key. Check OPENAI_API_KEY in your .env file.");
      (e as any).status = 401;
      throw e;
    }
    if (err?.message?.includes("ImageKit")) {
      throw new Error("Failed to save generated images to storage. Please try again.");
    }
    throw err;
  }
}

